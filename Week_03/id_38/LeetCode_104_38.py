class Solution:

    def maxDepth(self, root):
        if not root:
            return 0
        return self.get_tree_max_depth(root, 0)

    def get_tree_max_depth(self, root, depth):
        if not root:
            return depth
        return max(self.get_tree_max_depth(root.left, depth), self.get_tree_max_depth(root.right, depth)) + 1


